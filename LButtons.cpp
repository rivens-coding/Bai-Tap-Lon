//The window renderer
extern SDL_Renderer* gRenderer ;

extern int play ;

extern int help ;

extern int back_time ;

extern int monitor ;

extern int wrongs ;

extern int leave ;

extern int last_played ;

extern LTexture gButtonSpriteSheetTexture[ TOTAL_BUTTONS ];

LButton::LButton()
{
	mPosition.x = 0;
	mPosition.y = 0;

	mCurrentSprite = BUTTON_SPRITE_MOUSE_OUT;
}

void LButton::setPosition( int x, int y )
{
	mPosition.x = x;
	mPosition.y = y;
}

void LButton::render(int i,SDL_Rect* gSprite)
{
	//Show current button sprite
    gButtonSpriteSheetTexture[i].renderButtons( mPosition.x, mPosition.y, &gSprite[ mCurrentSprite ] );
}

void LButton::handlePlayButton( SDL_Event* e)
{
	//If mouse event happened
	if( e->type == SDL_MOUSEMOTION || e->type == SDL_MOUSEBUTTONDOWN || e->type == SDL_MOUSEBUTTONUP )
	{
		//Get mouse position
		int x, y;
		SDL_GetMouseState( &x, &y );

		//Check if mouse is in button
		bool inside = true;

		//Mouse is left of the button
		if( x < mPosition.x )
		{
			inside = false;
		}
		//Mouse is right of the button
		else if( x > mPosition.x + BUTTON_WIDTH )
		{
			inside = false;
		}
		//Mouse above the button
		else if( y < mPosition.y )
		{
			inside = false;
		}
		//Mouse below the button
		else if( y > mPosition.y + BUTTON_HEIGHT )
		{
			inside = false;
		}

		//Mouse is outside button
		if( !inside )
		{
			mCurrentSprite = BUTTON_SPRITE_MOUSE_OUT;
		}
		//Mouse is inside button
		else
		{
			//Set mouse over sprite
			switch( e->type )
			{
				case SDL_MOUSEMOTION:
                    mCurrentSprite = BUTTON_SPRITE_MOUSE_OVER_MOTION;
                    break;
				case SDL_MOUSEBUTTONUP :
                    play ++;
                    break;
			}
		}
	}
}

